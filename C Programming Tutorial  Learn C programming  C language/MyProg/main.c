#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Hello world!\n");
    printf("%d \n", 32); ///Integer
    printf("%ld \n", 32242412);///Large Integer
    printf("%f \n", 32.2);///Float
    printf("%lf \n", 32412124.2);///Large float
    printf("%c \n", 'a');///char
    printf("%s \n", "Hello world!");///string
    printf("%x \n", 16);///Hex

    puts("This is my c program");

    printf("The numbers are %d, %d and %d\n", 32, 58, 5);
    printf("%s\n", "test");
    printf("%c", 't');
    return 0;
}
